package application;

public class Produss {
	public int cod;
	public int cantitate;
	public String nume;
	public int pret;
	public String categorie;
	public Produss(int cod, String nume,int cantitate, int pret, String categorie) {
		super();
		this.cod = cod;
		this.cantitate = cantitate;
		this.nume = nume;
		this.pret = pret;
		this.categorie = categorie;
	}
	
}
